# Este programa define e chama uma função em Python.

# Primeiro, o programa define uma função chamada "saudacao".
# Uma função é um bloco de código que realiza uma tarefa específica.
# Você pode chamar a função sempre que quiser realizar essa tarefa.
def saudacao():
    # Dentro da função, o programa usa a função "print" para exibir uma mensagem.
    # A função "print" exibe o texto entre parênteses na tela.
    # Neste caso, a mensagem é "Olá, On33!".
    print("Olá, On33!")

# Depois de definir a função, o programa a chama.
# Chamar uma função significa dizer ao programa para executar o bloco de código dentro da função.
# Para chamar uma função, você usa o nome da função seguido por parênteses ().
# Neste caso, o programa chama a função "saudacao", então ele exibe a mensagem "Olá, On33!".
